package dto;

public class LoginUser {

	private int no;
	private String id;
	private String name;
	private String type;
	
	public LoginUser() {}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	@Override
	public String toString() {
		return "LoginUser [no=" + no + ", id=" + id + "]";
	}
	
	
}
